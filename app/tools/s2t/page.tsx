const t2sMap: Record<string, string> = Object.fromEntries(
  Object.entries(s2tMap).map(([s, t]) => [t, s])
);

function toTraditional(text: string): string {
  return text.split("").map((c) => s2tMap[c] || c).join("");
}

function toSimplified(text: string): string {
  return text.split("").map((c) => t2sMap[c] || c).join("");
}

