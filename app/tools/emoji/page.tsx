export default function EmojiTool() {
  const [search, setSearch] = useState("");
  const [copied, setCopied] = useState("");

  const copy = (emoji: string) => {
    navigator.clipboard.writeText(emoji);
    setCopied(emoji);
    setTimeout(() => setCopied(""), 1000);
  };

  const filtered = search
    ? Object.entries(emojiData).reduce<[string, string][]>((acc, [, cat]) => {
        cat.emojis.forEach((e) => { acc.push([e, cat.name]); });
        return acc;
      }, []).filter(([e]) => e.includes(search))
    : null;

  return (
    <ToolLayout title="Emoji 大全" description="分类查找复制 Emoji">
      <div className="space-y-4">
        <input value={search} onChange={(e) => setSearch(e.target.value)}
          placeholder="搜索 Emoji..."
          className="w-full p-3 rounded-lg border text-sm"
          style={{ background: "var(--card)", borderColor: "var(--border)" }} />

        {filtered ? (
          <div>
            <p className="text-sm mb-2" style={{ color: "var(--muted-foreground)" }}>找到 {filtered.length} 个</p>
            <div className="flex flex-wrap gap-1">
              {filtered.map(([e]) => (
                <button key={e} onClick={() => copy(e)}
                  className="text-2xl p-1.5 rounded-lg hover:bg-blue-100 dark:hover:bg-blue-900 transition-colors cursor-pointer"
                  title={e}>{copied === e ? "✓" : e}</button>
              ))}
            </div>
          </div>
        ) : (
          Object.entries(emojiData).map(([key, cat]) => (
            <div key={key}>
              <h3 className="font-semibold text-sm mb-2">{cat.name} ({cat.emojis.length})</h3>
              <div className="flex flex-wrap gap-1">
                {cat.emojis.map((e) => (
                  <button key={e} onClick={() => copy(e)}
                    className="text-2xl p-1 rounded hover:bg-blue-100 dark:hover:bg-blue-900 transition-colors cursor-pointer"
                    title={e}>{copied === e ? "✓" : e}</button>
                ))}
              </div>
            </div>
          ))
        )}
      </div>
    </ToolLayout>
  );
}
